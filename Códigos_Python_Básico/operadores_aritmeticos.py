# Calculos realizados com os operadores lógicos utilizados em Python

# Adição
soma = 2 + 5
print(soma)

# Subtração
sub = 5 - 2
print(sub)

# Multiplicação
multi = 2 * 5
print(multi)

# Divisão
divisao = 5 / 2
print(divisao)

# Divisão Inteira
divisao_inteira = 5 // 2
print(divisao_inteira)

# Resto da divisão
resto_div = 5 % 2
print(resto_div)

# Potenciação
pot = 2 ** 5
print(pot)
