"""Operadores relacionais são usados para comparar dois valores. Os valores a
serem comparados podem ser literais, variáveis ou expressões matemáticas."""

numero_1 = int(input('Informe o primeiro número: '))
numero_02 = int(input('Informe o segundo número: '))

print(numero_1, '==', numero_02, '->', numero_1 == numero_02)
print(numero_1, '!=', numero_02, '->', numero_1 != numero_02)
print(numero_1, '<', numero_02, '->', numero_1 < numero_02)
print(numero_1, '<=', numero_02, '->', numero_1 <= numero_02)
print(numero_1, '>', numero_02, '->', numero_1 > numero_02)
print(numero_1, '>=', numero_02, '->', numero_1 >= numero_02)
